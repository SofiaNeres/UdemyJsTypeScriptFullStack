// Concatenação
let num1 = '5';
let num2 = 10;
console.log(num1+num2); 

// Adição
num1 = 5;
num2 = 10;
console.log(num1+num2); 

// Subtração
console.log(5-10);

// Multiplicação
console.log(5*10);

// Divisão
console.log(10/5) 

// Potenciação
console.log(num1 ** num2); 

// Resto da divisão
num2 = 3;
console.log(num1%num2);

